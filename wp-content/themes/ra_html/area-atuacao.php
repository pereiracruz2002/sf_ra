        <h2>Direito Criminal e Penal</h2>
        <p class="txt-atuacao">O crime é a tipificação de um ou vários atos do ser humano, que ferem bens jurídicos de maior relevância.
           A pena é a resposta imposta pelo Estado ao indivíduo que age ferindo os bens jurídicos relevantes. Seja no ato tipificado ou na pena,
           a culpabilidade é fator primordial para punibilidade.<br><br>
            No Brasil, há o Código Penal e o Código de Processo Penal, além de outras diversas leis especiais, onde tipificam, delimitam os crimes previstos na legislação, e ainda, determinam as situações agravantes, atenuantes e nulidades, as quais servirão para que o réu seja julgado, podendo ser condenado ou absolvido, além disso, há o processo de execução penal, em sendo o réu condenado deverá cumprir sua pena, em regime fechado, semi-aberto, ou ainda, aberto.
            Há também a progressão de pena – desde que cumpridos alguns requisitos – onde o condenado será beneficiado.<br><br>
            Em todas estas situações, é importante o suposto réu ter um profissional preparado possibilitando-se a aplicação do princípio
            do contraditório e da ampla defesa, nos termos do artigo 5º, Inciso LV, da Constituição Federal, adotando todas as medidas legais cabíveis,
